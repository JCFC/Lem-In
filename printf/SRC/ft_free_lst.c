#include "../INC/ft_printf.h"

void				ft_free_lst(t_lst **lst)
{
	t_lst			*tmp;

	tmp = *lst;
	while (tmp)
	{
		*lst = (*lst)->next;
		free(tmp->str);
		free(tmp);
		tmp = *lst;
	}
}
